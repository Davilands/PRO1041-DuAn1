-- DROP PROC sp_NHANVIENXX
CREATE PROC sp_NHANVIENXX
AS BEGIN
	SELECT 
		TK.TENNV,
		COUNT(HD.MAHD) SL
	FROM HOADON HD 
	JOIN TAIKHOAN TK ON TK.TENDN = HD.TENDN
	GROUP BY TK.TENNV
	ORDER BY SL DESC
END
GO

